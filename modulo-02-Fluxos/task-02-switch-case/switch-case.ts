// -> Exemplo 1: Uso do Switch...Case
let numeroVerifica: number = 3;

switch (numeroVerifica) {
    case 1:
        console.log('Um');
        break;
    case 2:
        console.log('Dois');
        break;
    case 3:
        console.log('Três');
        break;
    default:
        console.log('Número inválido');
}